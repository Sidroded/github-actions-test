package org.example;

public class Calculator {
    public int sum(int i, int j) {
        return i + j;
    }

    public int multiply(int i, int j) {
        return i * j;
    }

    public int sub(int i, int j) {
        return i - j;
    }

    public double dev(int i, int j) {
        return (double) i / j;
    }
}
